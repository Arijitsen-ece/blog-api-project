import mongoose from "mongoose";

const postSchema = new mongoose.Schema({
  title: {
    type: String,
    required: true,
  },
  content: {
    type: String,
    required: true,
  },
  author: {
    type: String,
    required: true,
  },
  date: {
    type: String,
    default: () => new Date().toDateString(),
  },
});

const Post = mongoose.model("Post", postSchema);

export default Post;
